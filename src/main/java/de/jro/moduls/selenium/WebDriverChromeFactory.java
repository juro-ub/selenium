    public WebDriver getWebDriver(boolean headless, boolean showImage) throws Exception {
        ChromeOptions options = new ChromeOptions();
        int rnd = new Random().nextInt(userAgents.length);
        String userA = userAgents[rnd];
        if (userA != null && userA.length() > 0) {
            options.addArguments("user-agent=" + userA);
        }
        options.setExperimentalOption("excludeSwitches", Collections.singletonList("enable-automation"));
        options.setExperimentalOption("useAutomationExtension", false);
        options.addArguments("--disable-blink-features=AutomationControlled");
        options.addArguments("--no-sandbox");
        options.addArguments("--disable-dev-shm-usage"); // overcome limited resource problems
        options.addArguments("--start-maximized");
        // options.addArguments("--verbose");
        options.addArguments("--log-level=3");
        options.addArguments("--silent");
        // options.addArguments("--fast-start");
        options.addArguments("--disable-gpu");// disable because server has no good gpu
        options.addArguments("--disable-plugins");
        options.addArguments("--disable-notifications");
        // options.addArguments("--enable-fast-unload");
        // options.addArguments("--enable-experimental-canvas-features");
        options.addArguments("--use-simple-cache-backend");
        options.addArguments("--disable-session-crashed-bubble");
        options.addArguments("--noerrors");
        options.addArguments("--disable-password-generation");
        options.addArguments("--ignore-certificate-errors");
        options.addArguments("--disable-web-security");
        options.setPageLoadStrategy(PageLoadStrategy.EAGER);
        options.setExperimentalOption("excludeSwitches", Arrays.asList("disable-popup-blocking"));
        // options.addArguments("--incognito");
        options.addArguments("--disable-extensions");

        //Ignore headless because of clodflare protection
        if (headless) {
            //options.addArguments("--headless", "--disable-gpu");
            //options.addArguments("--window-size=1600,1200");
        }
        // options.setExperimentalOption("excludeSwitches",
        // Collections.singletonList("enable-automation"));
        Map<String, Object> prefs = new HashMap<String, Object>();
        prefs.put("credentials_enable_service", false);
        prefs.put("profile.password_manager_enabled", false);
        prefs.put("profile.default_content_settings.popups", false);
        prefs.put("profile.default_content_settings.geolocation", 2);
        if (showImage) {
            prefs.put("profile.default_content_setting_values.images", 1);
        } else {
            prefs.put("profile.default_content_setting_values.images", 2);
        }
        options.setExperimentalOption("prefs", prefs);
        options.addArguments("--disable-infobars");
        // create
        return new MyChromeDriver(options);
    }
